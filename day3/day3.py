import json


symbols = dict()
with open("day3.txt", "r") as f:
    for line in f.readlines():
        for char in line:
            if char not in ".0123456789":
                symbols[char] = dict()

with open("symbols.txt", "w") as f:
    json.dump(symbols, f)
